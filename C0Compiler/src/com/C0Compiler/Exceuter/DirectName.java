package com.C0Compiler.Exceuter;

//指令名称
public enum DirectName {

    LIT,//将常数值取到栈顶
    LOD,//将变量值取到栈顶
    STO,//将栈顶内容送入某变量单元中
    CAL,//调用函数
    INT,//在运行栈中为被调用的过程开辟数据区
    JMP,//无条件跳转
    JPC,//条件跳转
    ADD,//次栈顶与栈顶相加，退两个栈元素，结果值进栈
    SUB,//次栈顶减去栈顶，退两个栈元素，结果值进栈
    MUL,//次栈顶乘以栈顶，退两个栈元素，结果值进栈
    DIV,//次栈顶除以栈顶，退两个栈元素，结果值进栈
    RED,//从命令行读入一个输入置于栈顶
    WRT,//栈顶值输出至屏幕并换行
    RET //函数调用结束后,返回调用点并退栈
}
